#include <bits/stdc++.h>
using namespace std;


int main() {
    string str1 = "apple";
    string str2 = "banana";

    // Using comparison operators
    if (str1 < str2) {
        cout << str1 << " is less than " << str2 << endl;
    } else if (str1 > str2) {
        cout << str1 << " is greater than " << str2 << endl;
    } else {
        cout << str1 << " is equal to " << str2 << endl;
    }



    return 0;
}