bool check_prime(int n)
{
    bool is_prime = true;

    // 0 and 1 are not prime numbers
    if (n == 0 || n == 1)
    {
        is_prime = false;
    }

    for (int i = 2; i <= n / 2; ++i)
    {
        if (n % i == 0)
        {
            is_prime = false;
            break;
        }
    }

    return is_prime;
}

int F(int x)
{
    if (x == 0)
        return 0;
    else if (x == 1)
        return 1;
    else if (x == 2)
        return 2;
    else if (check_prime(x) && x % 2 == 1)
        return 0;
    else
    {
        for (int i = 2; i <= x; i++)
        {
            if (x % i == 0)
            {
                return F(i) + F(x / i);
            }
        }
    }
}

