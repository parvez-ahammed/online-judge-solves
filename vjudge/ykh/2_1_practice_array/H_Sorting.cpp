#include <iostream>
#include <bits/stdc++.h>
using namespace std;

#define SortA(ar, s) sort(ar, ar + s)

int main()
{
    int n;
    cin >> n;
    int arr[n];

    for (int i = 0; i < n; i++)
    {
        cin >> arr[i];
    }

    SortA(arr, n);

    for (int i = 0; i < n; i++)
    {
        cout << arr[i] << " ";
    }
    cout << endl;
    return 0;
}