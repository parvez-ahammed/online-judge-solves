void solve()
{
    int a, b, c;
    cin >> a >> b >> c;

    bool middle = ((a + c) % (2 * b) == 0);
    bool left = ((2 * b - a) > 0 and (2 * b - a) % c == 0);
    bool right = ((2 * b - c) > 0 and (2 * b - c) % a == 0);

    if (right or left or middle)
        cout << "YES" << endl;
    else
        cout << "NO" << endl;
}

