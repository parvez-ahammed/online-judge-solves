void solve()
{
    int a[3], b[3];
    cin >> a[0] >> a[1] >> a[2] >> b[0] >> b[1] >> b[2];

    sort(a, a + 3);
    sort(b, b + 3);

    int alice = a[1] + a[2];
    int bob = b[1] + b[2];

    if (alice == bob)

        cout << "Tie";
    
    else if (alice > bob)
        cout << "Alice";
    else
        cout << "Bob";
    cout << endl;
}

